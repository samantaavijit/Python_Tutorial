# Arithmetic operator
print("5 + 6 =", 5 + 6)
print("5 - 6 =", 5 - 6)
print("5 * 6 =", 5 * 6)
print("15 / 6 =", 15 / 6)
print("15 // 6 =", 15 // 6)
print("5 % 6 =", 5 % 6)
print("5 ** 3 =", 5 ** 3)
# Assignment Operator
print("___________________________-------------------------------------------___________________________")
x = 5
print(x)
x += 10
print(x)
# Comparison operator
print("___________________________-------------------------------------------___________________________")
a = 10
b = 5
print(a == b)
# Logical operator
print("___________________________-------------------------------------------___________________________")
a = True
b = False
print(a and b)
print(a or b)
# Identity operator
print("___________________________-------------------------------------------___________________________")
print(a is b)
print(a is not b)
print(5 is not 5)
# Membership operator
print("___________________________-------------------------------------------___________________________")
number = [3, 25, 85, 69, 100, 5, 1, 2, 0, 9]
print(100 in number)
print(100 not in number)
# Bitwise operator
print("___________________________-------------------------------------------___________________________")
print(0 & 1)
print(0 | 1)
